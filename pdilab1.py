print(tiempo[:5])

"""### 3.2. Velocidad instantánea

Cálculo de la velocidad instantánea del vehículo utilizando diferencias finitas hacia adelante entre fotogramas consecutivos.
"""

# Implementación para el cálculo de la velocidad instantánea en metros/segundo

delta_t = 1 / fps
velocidades_metros_s = np.diff(posiciones_x_metros) / delta_t

# Para igualar la longitud con las posiciones, se añade el último valor o un cero al final.
