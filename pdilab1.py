"""## PUNTO 2 (40%) - Detección y Segmentación del Vehículo

Esta sección implementa los pasos para detectar y segmentar el vehículo en cada fotograma del video. Incluye:
- **Conversión a escala de grises:** Para facilitar la segmentación.
- **Sustracción de fondo (background subtraction):** Para aislar el vehículo en movimiento.
- **Operaciones morfológicas (apertura y cierre):** Para limpiar la máscara del vehículo y eliminar ruido.
- **Detección de contornos:** Para identificar las formas de los objetos en movimiento.
- **Cálculo del centroide:** Para determinar la posición del vehículo en cada fotograma.

### 2.1 Configuración Inicial para Detección y Segmentación
"""

import cv2
import numpy as np
from google.colab.patches import cv2_imshow

# Definir la ruta del video (asegúrate de que este archivo esté cargado en Colab)
video_path = "CarroNocortado.mp4" # Definición de la ruta del video

# Inicializar la captura de video
cap = cv2.VideoCapture(video_path)

# Inicialización del sustractor de fondo
fgbg = cv2.createBackgroundSubtractorMOG2()

# Lista para almacenar los centroides del vehículo
centroides = []

# --- Variables para definir la Región de Interés (ROI) ---
# Estas coordenadas definen la zona donde nos interesa detectar el vehículo.
# Todo lo que esté fuera de esta zona será ignorado en el procesamiento.
# La resolución original del video es 1280x720.
y_inicio = 406    # Inicio del recorte vertical (fila superior) de la ROI
y_fin = 675       # Fin del recorte vertical (fila inferior) de la ROI
x_inicio = 9      # Inicio del recorte horizontal (columna izquierda) de la ROI
x_fin = 1280      # Fin del recorte horizontal (columna derecha) de la ROI

# Definir el kernel para las operaciones morfológicas
kernel = np.ones((7,7),np.uint8)

# Umbral mínimo de área para los contornos (en píxeles cuadrados)
min_contour_area = 500  # Ignora objetos menores a 500 píxeles cuadrados

"""### 2.2 Bucle de Procesamiento de Video (Detección y Tracking)

---


"""

# Este bucle utiliza las variables inicializadas en la celda '2.1 Configuración Inicial'
while True:

    ret, frame = cap.read()

    if not ret:
        break

    # Crear una copia del frame original para dibujar sobre ella
    frame_display = frame.copy()

    # 1. Conversión a escala de grises (aplicada al frame completo)
    gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)

    # 2. Sustracción de fondo (aplicada al frame completo en escala de grises)
    fgmask = fgbg.apply(gray)

    # 3. Operaciones morfológicas para limpiar la máscara (aplicadas al fgmask completo)
    mask_limpia = cv2.morphologyEx(fgmask, cv2.MORPH_OPEN, kernel)
    mask_limpia = cv2.morphologyEx(mask_limpia, cv2.MORPH_CLOSE, kernel)

    # --- Aplicar la Región de Interés (ROI) a la máscara limpia ---
    # Creamos una máscara de ceros del mismo tamaño que mask_limpia
    roi_mask_region = np.zeros(mask_limpia.shape, dtype=np.uint8)
    # Dibujamos un rectángulo blanco (255) dentro de nuestra ROI
    roi_mask_region[y_inicio:y_fin, x_inicio:x_fin] = 255
    # Aplicamos esta máscara a mask_limpia. Todo lo que esté fuera de la ROI se volverá negro.
    mask_limpia = cv2.bitwise_and(mask_limpia, roi_mask_region)

    # Convertir mask_limpia (ahora con la ROI aplicada) a BGR para poder concatenarla y mostrarla
    mask_limpia_bgr = cv2.cvtColor(mask_limpia, cv2.COLOR_GRAY2BGR)

    # 4. Detección de contornos (solo detectará contornos dentro de la ROI debido a la máscara)
    contours, _ = cv2.findContours(mask_limpia, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    if contours:

        # Encontrar el contorno más grande que sea mayor al área mínima
        largest_contour = None
        max_area = 0
        for contour in contours:
            area = cv2.contourArea(contour)
            # Aseguramos que el contorno esté dentro de la ROI (aunque la máscara ya lo filtra, es una doble verificación)
            # y que sea mayor al área mínima
            if area > min_contour_area and area > max_area:
                # Aquí podríamos añadir una comprobación de que el centroide del contorno esté dentro de la ROI
                # pero la máscara ya hace gran parte del trabajo.
                largest_contour = contour
                max_area = area

        if largest_contour is not None:

            # 5. Cálculo del centroide del vehículo
            M = cv2.moments(largest_contour)

            if M["m00"] != 0:

                cx = int(M["m10"] / M["m00"])
                cy = int(M["m01"] / M["m00"])

                centroides.append((cx, cy))

                # --- Dibujo en el frame_display original para visualización ---
                # Dibujar el círculo del centroide actual
                cv2.circle(frame_display, (cx, cy), 6, (0, 0, 255), -1) # Color rojo para el centroide

                # Dibujar la trayectoria (líneas entre centroides)
                for i in range(1, len(centroides)):
                    cv2.line(frame_display, centroides[i-1], centroides[i], (0, 255, 0), 2) # Color verde para la trayectoria

                # Dibujar el contorno del objeto detectado
                cv2.drawContours(frame_display, [largest_contour], -1, (255, 0, 0), 2) # Color azul para el contorno

                texto = f"Centroide ({cx},{cy})"

                cv2.putText(frame_display, texto, (cx+10, cy-10),
                            cv2.FONT_HERSHEY_SIMPLEX,
                            0.6,
                            (0, 0, 0),
                            2) # Color negro para el texto

    # Concatenar el frame original con los dibujos y la máscara procesada para mostrarlos juntos
    concatenated_frames = cv2.hconcat([frame_display, mask_limpia_bgr])

    cv2_imshow(concatenated_frames)

cap.release()

