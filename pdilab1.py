"""### 3.3. Aceleración instantánea

Cálculo de la aceleración instantánea como la segunda derivada numérica de la posición.
"""

# Implementación para el cálculo de la aceleración instantánea en metros/segundo²

delta_t = 1 / fps
aceleraciones_metros_s2 = np.diff(velocidades_metros_s) / delta_t

